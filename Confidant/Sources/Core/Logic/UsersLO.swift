public class UsersLO {

//*************************************************
// MARK: - Properties
//*************************************************

	public var current: UserVO?
	
	static public let instance: UsersLO = UsersLO()
	
//*************************************************
// MARK: - Constructors
//*************************************************

//*************************************************
// MARK: - Protected Methods
//*************************************************

//*************************************************
// MARK: - Exposed Methods
//*************************************************
	
	public func authenticate(by method: AuthenticationManager.Method, completionHandler: @escaping LogicResult) {
		let authenticationManager = AuthenticationManager(method: method)
		
		authenticationManager.execute() { (user, logicResult) in
			self.current = user
			completionHandler(logicResult)
		}
	}

//*************************************************
// MARK: - Overridden Public Methods
//*************************************************

}

