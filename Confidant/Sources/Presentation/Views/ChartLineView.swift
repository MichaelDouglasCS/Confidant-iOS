@IBDesignable
class ChartLineView: ChartColumnView {

//**************************************************
// MARK: - Properties
//**************************************************

	@IBInspectable var lastValue: CGFloat = 1.0
	
	override var bezierPath: UIBezierPath {
		let last = self.lastValue.clamped(min: 0.0, max: 1.0)
		let current = self.value.clamped(min: 0.0, max: 1.0)
		let size = self.bounds.size
		let start = (1.0 - last) * size.height
		let end = (1.0 - current) * size.height
		
		let path = UIBezierPath()
		path.move(to: CGPoint(x: 0.0, y: start))
		path.addLine(to: CGPoint(x: size.width, y: end))
		
		return path
	}
	
//**************************************************
// MARK: - Constructors
//**************************************************

//**************************************************
// MARK: - Protected Methods
//**************************************************

//**************************************************
// MARK: - Exposed Methods
//**************************************************
	
//**************************************************
// MARK: - Overridden Methods
//**************************************************
	
}
